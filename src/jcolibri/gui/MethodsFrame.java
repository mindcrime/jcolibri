	private void removeMethod(int idx) {
		String pkgName = (String) packagesComboBox.getSelectedItem();
		PrototypeMethodPkgReg pmPkgReg = PrototypeMethodsRegistry.getInstance()
				.getPrototypeMethodPkg(pkgName);
		try {
			pmPkgReg.removeMethod(idx);
			pmPkgReg.storeRegistry();
		} catch (jcolibri.cbrcore.exception.InternalException ie) {
			JOptionPane.showMessageDialog(this, "Error while storing methods",
					"Error", JOptionPane.ERROR_MESSAGE);
		}
	}

	private void addMethod() {
		String pkgName = (String) packagesComboBox.getSelectedItem();
		PrototypeMethodPkgReg pmPkgReg = PrototypeMethodsRegistry.getInstance()
				.getPrototypeMethodPkg(pkgName);
		CBRMethod method = new CBRMethod() {
			private static final long serialVersionUID = 1L;

			public CBRContext execute(CBRContext context)
					throws ExecutionException {
				return null;
			}
		};
		JInternalFrame intf = new EditMethodFrame(false, pmPkgReg, method);
		this.getDesktopPane().add(intf);
		intf.show();
	}

	private void editMethod() {
		if (methodsTable.getSelectedRow() >= 0) {
			String pkgName = (String) packagesComboBox.getSelectedItem();
			PrototypeMethodPkgReg pmPkgReg = PrototypeMethodsRegistry
					.getInstance().getPrototypeMethodPkg(pkgName);
			CBRMethod method;
			method = ((MethodsTableModel) methodsTable.getModel())
					.getMethod(methodsTable.getSelectedRow());
			JInternalFrame intf = new EditMethodFrame(true, pmPkgReg, method);
			this.getDesktopPane().add(intf);
			intf.show();
		}
	}

