	private void updatePackages() {
		Iterator it;
		PackageInfo pkgInfo;

		packagesComboBox.removeAllItems();
		it = PackageManager.getInstance().getActivePackages().iterator();
		while (it.hasNext()) {
			pkgInfo = (PackageInfo) it.next();
			packagesComboBox.addItem(pkgInfo.getName());
		}
	}

